
public class TreasureCardLevelData
{
    public int gold;
    public int mana;
    public int effectIndex;
    public string effectValue;

    public TreasureCardLevelData(int gold, int mana, int effectIndex, string effectValue)
    {
        this.gold = gold;
        this.mana = mana;
        this.effectIndex = effectIndex;
        this.effectValue = effectValue;
    }
}